#include "3-calc.h"
/**
 * op_add - Adds two integers
 * @a: integer
 * @b: integer
 * Return: addtion of two integer
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Substracts two integers
 * @a: integer
 * @b: integer
 * Return: substraction of two integer
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer
 * @b: integer
 * Return: multiplication of two integer
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: integer
 * @b: integer
 * Return: division of two integer
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two integers
 * @a: integer
 * @b: integer
 * Return: modulus of two integer
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}

